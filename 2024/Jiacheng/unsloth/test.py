import matplotlib.pyplot as plt
import numpy as np


def plot_gradients(gradients):
    # 将参数值转换为一个列表
    plt.figure(figsize=(100, 25))
    values = [param.cpu().numpy().mean() for param in gradients.values()]
    names = list(gradients.keys())
    
    # 创建条形图
    plt.bar(names,values)
    plt.xticks(rotation='vertical', fontsize='small')
    plt.xlabel('Gradient value')
    plt.ylabel('Parameter name')
    plt.tight_layout()
    plt.savefig("gradients.pdf")
    

plot_gradients(trainer.gradients)

